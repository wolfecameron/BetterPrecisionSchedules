class QBertPooler(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = QLinear(config.hidden_size, config.hidden_size)
        self.activation = nn.Tanh()

    def forward(self, hidden_states, num_bits, num_grad_bits):
        # We "pool" the model by simply taking the hidden state corresponding
        # to the first token.
        first_token_tensor = hidden_states[:, 0]
        pooled_output = self.dense(first_token_tensor, num_bits, num_grad_bits)
        pooled_output = self.activation(pooled_output)
        return pooled_output

class QBertSelfOutput(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = QLinear(config.hidden_size, config.hidden_size)
        self.LayerNorm = nn.LayerNorm(config.hidden_size, eps=config.layer_norm_eps)
        self.dropout = nn.Dropout(config.hidden_dropout_prob)

    def forward(self, hidden_states, input_tensor, num_bits, num_grad_bits):
        hidden_states = self.dense(hidden_states, num_bits, num_grad_bits)
        hidden_states = self.dropout(hidden_states)
        hidden_states = self.LayerNorm(hidden_states + input_tensor)
        return hidden_states


class QBertAttention(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.self = QBertSelfAttention(config)
        self.output = QBertSelfOutput(config)

    def forward(
        self,
        hidden_states,
        num_bits,
        num_grad_bits,
        attention_mask=None,
        head_mask=None,
        encoder_hidden_states=None,
        encoder_attention_mask=None,
        output_attentions=False,
    ):
        self_outputs = self.self(hidden_states, num_bits, num_grad_bits, attention_mask,
                head_mask, encoder_hidden_states, encoder_attention_mask, output_attentions)
        attention_output = self.output(self_outputs[0], hidden_states, num_bits, num_grad_bits)
        outputs = (attention_output,) + self_outputs[1:]  # add attentions if we output them
        return outputs

class QBertIntermediate(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = QLinear(config.hidden_size, config.intermediate_size)
        # assert not isinstance(config.hidden_act, str)
        if config.hidden_act == 'gelu':
            self.intermediate_act_fn = torch.nn.GELU()
        else:
            raise NotImplementedError()

    def forward(self, hidden_states, num_bits, num_grad_bits):
        hidden_states = self.dense(hidden_states, num_bits, num_grad_bits)
        hidden_states = self.intermediate_act_fn(hidden_states)
        return hidden_states

class QBertOutput(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.dense = QLinear(config.intermediate_size, config.hidden_size)
        self.LayerNorm = nn.LayerNorm(config.hidden_size, eps=config.layer_norm_eps)
        self.dropout = nn.Dropout(config.hidden_dropout_prob)

    def forward(self, hidden_states, input_tensor, num_bits, num_grad_bits):
        hidden_states = self.dense(hidden_states, num_bits, num_grad_bits)
        hidden_states = self.dropout(hidden_states)
        hidden_states = self.LayerNorm(hidden_states + input_tensor)
        return hidden_states

class QBertLayer(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.chunk_size_feed_forward = config.chunk_size_feed_forward
        self.seq_len_dim = 1
        self.attention = QBertAttention(config)
        self.is_decoder = config.is_decoder
        self.add_cross_attention = config.add_cross_attention
        assert not self.add_cross_attention
        #if self.add_cross_attention:
        #    assert self.is_decoder, f"{self} should be used as a decoder model if cross attention is added"
        #    self.crossattention = BertAttention(config)
        self.intermediate = QBertIntermediate(config)
        self.output = QBertOutput(config)

    def forward(
        self,
        hidden_states,
        num_bits,
        num_grad_bits,
        attention_mask=None,
        head_mask=None,
        encoder_hidden_states=None,
        encoder_attention_mask=None,
        output_attentions=False,
    ):
        self_attention_outputs = self.attention(
            hidden_states,
            num_bits,
            num_grad_bits,
            attention_mask,
            head_mask,
            output_attentions=output_attentions,
        )
        attention_output = self_attention_outputs[0]
        outputs = self_attention_outputs[1:]  # add self attentions if we output attention weights

        # if self.is_decoder and encoder_hidden_states is not None:
        #     assert hasattr(
        #         self, "crossattention"
        #     ), f"If `encoder_hidden_states` are passed, {self} has to be instantiated with cross-attention layers by setting `config.add_cross_attention=True`"
        #     cross_attention_outputs = self.crossattention(
        #         attention_output,
        #         attention_mask,
        #         head_mask,
        #         encoder_hidden_states,
        #         encoder_attention_mask,
        #         output_attentions,
        #     )
        #     attention_output = cross_attention_outputs[0]
        #     outputs = outputs + cross_attention_outputs[1:]  # add cross attentions if we output attention weights

        layer_output = lp_apply_chunking_to_forward(self.feed_forward_chunk, self.chunk_size_feed_forward,
                self.seq_len_dim, num_bits, num_grad_bits, attention_output)
        outputs = (layer_output,) + outputs
        return outputs

    def feed_forward_chunk(self, attention_output, num_bits, num_grad_bits):
        intermediate_output = self.intermediate(attention_output, num_bits, num_grad_bits)
        layer_output = self.output(intermediate_output, attention_output, num_bits, num_grad_bits)
        return layer_output

